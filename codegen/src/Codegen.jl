module Codegen

using Base64: base64decode
using GitHub: GitHub
using JSON: JSON
using JuliaFormatter: format, format_file
using Mocking: Mocking, @mock
using OrderedCollections: LittleDict, OrderedDict

const AUTO_GENERATED_SIGNATURE = "This file is auto-generated by AWS.jl"
const PKG_DIR = abspath(joinpath(@__DIR__, "..", ".."))
const HIGH_LEVEL_SERVICES_DIR = joinpath(PKG_DIR, "src", "services")
const LOW_LEVEL_SERVICES_FILE = joinpath(PKG_DIR, "src", "AWSServices.jl")

include("utilities.jl")
include("exceptions.jl")
include("high_level.jl")
include("low_level.jl")

"""
    parse_aws_metadata()

Generate low-level and high-level Julia wrappers for each AWS service definition found in
[`aws-sdk-js-v3` GitHub Repository](https://github.com/aws/aws-sdk-js-v3/tree/main/codegen/sdk-codegen/aws-models).

Low-level API wrappers are defined in `$(relpath(LOW_LEVEL_SERVICES_FILE, PKG_DIR))` and
high-level API wrappers are each defined separate files within the directory
`$(relpath(HIGH_LEVEL_SERVICES_DIR, PKG_DIR))`.
"""
function parse_aws_metadata()
    auth = GitHub.authenticate(ENV["GITHUB_AUTH"])

    service_files = _get_service_files(auth)

    _generate_low_level_wrappers(service_files, auth)
    format_file(LOW_LEVEL_SERVICES_FILE)

    _generate_high_level_wrapper(service_files, auth)
    format(HIGH_LEVEL_SERVICES_DIR; verbose=true)

    return nothing
end

end
