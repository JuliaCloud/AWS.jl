const AUTO_GENERATED_SIGNATURE = "This file is auto-generated by AWS.jl"
const PKG_DIR = abspath(joinpath(@__DIR__, "..", ".."))
const HIGH_LEVEL_SERVICES_DIR = joinpath(PKG_DIR, "src", "services")
const LOW_LEVEL_SERVICES_FILE = joinpath(PKG_DIR, "src", "AWSServices.jl")

include("utilities.jl")
